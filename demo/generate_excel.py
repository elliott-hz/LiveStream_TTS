BODY_F = Font(name="Microsoft YaHei", size=10, color="334155")
BOLD_F = Font(name="Microsoft YaHei", bold=True, size=10, color="1E293B")
NOTE_F = Font(name="Microsoft YaHei", size=9, color="64748B")
CODE_F = Font(name="Consolas", size=9, color="334155")

THIN = Border(left=Side("thin","CBD5E1"), right=Side("thin","CBD5E1"), top=Side("thin","CBD5E1"), bottom=Side("thin","CBD5E1"))
C = Alignment(horizontal="center", vertical="center", wrap_text=True)
L = Alignment(horizontal="left", vertical="center", wrap_text=True)

LIGHT = PatternFill(start_color="F1F5F9", end_color="F1F5F9", fill_type="solid")
GREEN = PatternFill(start_color="F0FDF4", end_color="F0FDF4", fill_type="solid")
BLUE = PatternFill(start_color="F0F9FF", end_color="F0F9FF", fill_type="solid")

ROW = 1

def r_merge(text, font=None, fill=None, h=20, c1=1, c2=11):
    global ROW
    ws.merge_cells(start_row=ROW, start_column=c1, end_row=ROW, end_column=c2)
    cell = ws.cell(row=ROW, column=c1, value=text)
    cell.font = font or BODY_F
    cell.alignment = L
    if fill: cell.fill = fill
    ws.row_dimensions[ROW].height = h
    ROW += 1

def title(text):
    r_merge(text, TITLE_F, h=28)

def h2(text):
    r_merge(text, H2_F, LIGHT, h=24)

def h3(text):
    r_merge(text, H3_F, h=22, c1=2, c2=11)

def note(text):
    r_merge(text, NOTE_F, BLUE, h=22)

def code(text):
    r_merge(text, CODE_F, LIGHT, h=18, c1=2, c2=10)

def br():
    global ROW; ROW += 1

def tbl_header(headers):
    global ROW
    for i, h in enumerate(headers, 1):
        c = ws.cell(row=ROW, column=i, value=h)
        c.font = HDR_F; c.fill = HDR_FILL; c.alignment = C; c.border = THIN
    ws.row_dimensions[ROW].height = 22
    ROW += 1

def tbl_row(vals, bold=False, green=False, h=22):
    global ROW
    for i, v in enumerate(vals, 1):
        c = ws.cell(row=ROW, column=i, value=v)
        c.font = BOLD_F if bold else BODY_F
        if green: c.fill = GREEN
        c.alignment = C; c.border = THIN
    ws.row_dimensions[ROW].height = h
    ROW += 1

